<h4><a name="TOC-What-happens-to-data-after-it-is-submitted-"></a>What happens to data after it is submitted?</h4>We will host your data on our private GigaDB server giving access to the reviewers, if the manuscript is accepted we will move the data to our public production server. If it is unsuccessful the data will be deleted.

<br />
