<h4><a name="TOC-How-do-I-cite-the-data-in-my-manuscript-"></a>How do I cite the data in my manuscript?</h4><i>GigaScience</i> supports and has signed the <a href="https://www.force11.org/group/joint-declaration-data-citation-principles-final" target="_blank">FORCE 11 Joint Declaration of the Data Citation Principles</a>, feeling strongly that data should be accorded the same importance in the scholarly record as citations of publications. GigaDB datasets can and should be cited in the same manner as any other reference, although the format is journal specific based on their instructions. Following DCC and DataCite guildelines, in GigaScience journal the citation within the references section will be of the form:Author List; publication year: "Dataset title", GigaScience Database. DOI. Example.Peter E Larsen; Yang Dai; (2015): Supporting materials for "Metabolome of Human gut microbiome is predictive of host dysbiosis".; GigaScience Database. http://dx.doi.org/10.5524/100163

