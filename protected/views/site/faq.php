<h4><a name="TOC-What-is-a-GigaDB-DOI-"></a>What is a GigaDB DOI?</h4>A Digial Object Identifier (DOI) is a stable, citable link to an electronic resource. A GigaDB DOI is a stable and citable link to a dataset hosted by GigaDB. 

<br />
<br />

<h4><a name="TOC-Why-does-GigaDB-use-Creative-Commons-Zero-"></a>Why does GigaDB use Creative Commons Zero?</h4>It is widely recognized that publicly funded research data should be made publicly available for free to be used by anyone. The Creative Commons Zero (CC0) waiver provides the explicit statement of that fact, and it is transparent to all that the data hosted by GigaDB are all freely available for any use case. CC0 is thought to be the most appropriate method for dedicating data to the public domain, but for more on the rationale and practicalities <a href="http://www.biomedcentral.com/1756-0500/5/494" target="_blank">see this <i>BMC Research Notes</i> editorial</a>. Citation of data usage is greatly encouraged in order to provide the recognition to the data producers, both for their efforts in the production and in their foresight and generosity in making the data CC0.

