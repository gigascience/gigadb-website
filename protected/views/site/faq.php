<h4><a name="TOC-What-is-GigaDB-"></a>What is GigaDB?</h4>

GigaDB is the home for all data/files/tools/software associated with <i>GigaScience </i>manuscripts. GigaDB curators will ensure the information is complete and appropriately formatted, before cataloging and publishing. Submission of data to GigaDB complements but does not serve as a replacement for community approved public repositories, supporting data and source code should still be made publicly available in a suitable public repository. GigaDB can link any and all publicly deposited data together with additional files/tools that do not have a natural home in any other public repository. 

<br />
<br />

<h4><a name="TOC-What-journals-are-integrated-with-GigaDB-"></a>What journals are integrated with GigaDB?</h4>
At the present only <i>GigaScience</i>.

<br />
<br />

<h4><a name="TOC-Why-use-GigaDB-"></a>Why use GigaDB?</h4>
<i>GigaScience</i> is committed to enabling reproducible science, to do this, readers need to be able to easily find and get hold off all the underlying data, methods, workflows, software and anything else that was used in the research. In the past authors of research articles have made (justified) claims that there is no way of making all their data available, now GigaDB has filled that gap. GigaDB complements but does not serve as a replacement for community approved public repositories, and can link any and all publicly deposited data together with additional files/tools that do not have a natural home in any other public repository. Any and all data/files required for reproducibility of <i >GigaScience </i>manuscripts should be either hosted in, or linked to from, GigaDB.

<br />
<br />

<h4><a name="TOC-What-kinds-of-data-does-GigaDB-accept-"></a>What kinds of data does GigaDB accept?</h4>
Anything related to a <i>GigaScience </i>article that does NOT already have a relevant public repository (e.g. sequence data should still be deposited in the INSDC archives and/or the SRA).

<br />
<br />

<h4><a name="TOC-My-research-is-on-human-subjects.-Can-I-archive-my-data-in-GigaDB-"></a>My research is on human subjects. Can I archive my data in GigaDB?</h4>
As long as the data is fully consented and legally and ethically approved for public release, we encourage complete disclosure including a blank copy of the consent form signed where possible.

<br />
<br />

<h4><a name="TOC-In-what-file-format-s-should-I-submit-my-data-"></a>In what file format(s) should I submit my data?</h4>
There are many specific formats depending on the date types, but the rule of thumb is to use non-propitiatory formats and where possible follow the standard for the relevant field, our curators will be on hand to help with any specific questions on this matter.

<br />
<br />

<h4><a name="TOC-When-should-I-submit-my-data-"></a>When should I submit my data?</h4>At the same time or soon after submission of the manuscript. <i>GigaScience </i>reviewers will be looking to see if the underlying data is available and appropriate, so they will need access to it. This can be via your own private servers if you prefer, but we offer a secure staging area to host data under review/pre-release.

<br />
<br />

<h4><a name="TOC-How-can-I-modify-files-I-have-submitted-to-GigaDB-while-my-article-is-in-review-"></a>How can I modify files I have submitted to GigaDB while my article is in review?</h4>While the dataset is "pre-release" status, any files can be replaced by over-writing the original file with the newly modified one. After publishing the data, no over-writing will be permissible, only the addition of new files, all published files will remain available (unless there is a very good reason to remove them). Versioning is still possible for updates and major changes to the files if they need to be changed post-publication.

<br />
<br />

<h4><a name="TOC-What-should-I-prepare-before-submission-"></a>What should I prepare before submission?</h4>Organise your data into logical directories/folders, name files consistently and <b>without using spaces or special characters</b>. Re-read you methods section to check that everything mentioned there is available, either by links to public repositories or as files you have organised to submit to GigaDB.

<br />
<br />

<h4><a name="TOC-How-can-I-make-my-data-submission-as-accessible-and-reusable-as-possible-"></a>How can I make my data submission as accessible and reusable as possible?</h4>By ensuring all files are in non-proprietary formats that anyone can use without the need for expensive software.By making sure data tables are in tables <i>not PDF</i>.By using a CC0 waiver or other suitable public domain licenses for datasets. By using OSI (Open Source Initiative) licenses for software, and linking to versions in code repositories for updates and forking.By including as much metadata about the samples/specimens/files/methods etc... as possible.

<br />
<br />

<h4><a name="TOC-How-do-I-submit-data-"></a>How do I submit data?</h4>All data submissions should be approved before being started, please contact <a href="mailto:editorial@gigasciencejournal.com">editorial@gigasciencejournal.com</a> to discuss your article and associated data with our editors.Once approved, there are two possible routes to provide the metadata about your data:
<ol><li> use the online <a href="http://gigadb.org/dataset/upload">submission wizard</a> - this is a good option for datasets with few authors, and few files. The wizard currently does not have functionality to upload tabular information so everything must be typed in individually.</li>
<li> use the template spreadsheet (excel, but compatible with open office too) downloadable from <a href="http://www.google.com/url?q=http%3A%2F%2Fgigadb.org%2Ffiles%2FGigaDBUploadForm.xls&amp;sa=D&amp;sntz=1&amp;usg=AFrqEzf6cPyp-vGxxVTE9OEq2TPsJqU7fg">here </a>- This option is better where there are multiple authors and/or multiple files and/or samples. NB. the spreadsheet contains macros, but these are only to allow the forward and back buttons to work so can be disabled, you can just click the relevant tabs at the bottom of the spreadsheet.
</li></ol>
For more details on submitting using the Spreadsheet please see here.

