<h4><a name="TOC-Are-there-any-problems-with-publishing-my-final-research-paper-AFTER-publishing-the-data-in-GigaScience-"></a>Are there any problems with publishing my final research paper AFTER publishing the data in <i>GigaScience</i>?</h4>There shouldn't be, and a major rationale for data publishing is to incentivise earlier release of data in this manner. It is commonly understood throughout the publishing community that publishing data (as a Data Note or in a public archive) is a good thing to be encouraged, and as such, there are no penalties to then subsequently publishing research based on those data. <i >GigaScience</i> has published plenty of data notes and released data sets prior to the analysis papers being published, some examples are: 

<ul>
<li>3,000 Rice Genomes Project (13.4 Tb data).
</li>
<li>Polar Bear genome - dataset released in GigaDB nearly 3 years before the analysis paper was published in <i>Cell</i>.
</li>
<li>Deadly 2011 outbreak <a href="http://dx.doi.org/10.5524/100001" target="_blank"><i>E. Coli</i> genome</a> that lead to over 50 deaths in Germany (and eventually published in <i>NEJM</i>).
</li>
</ul>

<p >Our Polar bear genome data was released nearly three years before any official publication came out from the project, and despite being used by at least 5 other studies, the analysis paper made the cover of <i>Cell</i> (see <a href="http://blogs.biomedcentral.com/gigablog/2014/05/14/the-latest-weapon-in-publishing-data-the-polar-bear/" target="_blank">the blog</a> for more). </p>
<p >Journals do not consider the publication of a dataset with a DOI and associated protocol information as a 'prior publication' that would preclude subsequent publication of new results obtained from such a dataset. F1000 Research did a useful survey to confirm this with a number of publishers (see: <a href="http://f1000research.com/data-policies" target="_blank"> F1000 policy</a>), and this is only going to become increasingly observed and accepted as most of the publishers are now promoting their own Data Journals.</p>

