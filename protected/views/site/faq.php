<h4><a name="TOC-What-is-Hypothes.is-"></a>What is Hypothes.is ? </h4><h4><a name="TOC-1"></a><a href="http://hypothes.is/" imageanchor="1"  target="_blank"><img alt="http://hypothes.is" border="0" src="https://sites.google.com/a/gigasciencejournal.com/gigascience/_/rsrc/1447915702699/GigaDB/faqs/hypothesis.png"  /></a></h4><a href="http://gigadb.org/images/hypothesis.png">Hypothes.is</a> is an open source project helping to bring a discussion, annotation and curation layer to the web, we are collaborating with Hypothes.is in order to make all our datasets open to discussion by anyone (with a hypothesis user account). Simply highlight the text of interest on the page and click "New Note" icon that appears. To see previous notes, click the number on the side bar, or open the side bar to see all preious public annotations.

